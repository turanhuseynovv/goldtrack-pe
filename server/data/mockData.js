// Tüm dataset üzerinden max/min değerler dinamik olarak çıkarılır
const maxValues = {
    reserve: Math.max(...mines.map(m => m.reserveTon)),
    grade:   Math.max(...mines.map(m => m.goldGradeGt)),
    riskMin: Math.min(...mines.map(m => m.envRiskLevel)),
    riskMax: Math.max(...mines.map(m => m.envRiskLevel))
};

/**
 * Kurumsal Yatırım Skoru (0–100)
 * Rezerv: 50 puan
 * Grade: 30 puan
 * Risk: 20 puan (ters orantılı)
 */
export function computeInvestmentScore(mine) {

    // 1) Rezerv – 50 puan
    const reserveNorm = mine.reserveTon / maxValues.reserve;
    const reserveScore = reserveNorm * 50;

    // 2) Grade – 30 puan
    const gradeNorm = mine.goldGradeGt / maxValues.grade;
    const gradeScore = gradeNorm * 30;

    // 3) Risk – 20 puan (ne kadar düşükse o kadar iyi)
    const riskNorm = (maxValues.riskMax - mine.envRiskLevel) /
                     (maxValues.riskMax - maxValues.riskMin);
    const riskScore = riskNorm * 20;

    // Toplam Skor
    return reserveScore + gradeScore + riskScore;
}
